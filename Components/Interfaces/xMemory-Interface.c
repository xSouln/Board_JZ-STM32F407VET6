//==============================================================================
//includes:

#include "Common/xMemory.h"
//==============================================================================
//variables:

SemaphoreHandle_t xMemoryMutex;
//==============================================================================
//functions:

void xMemoryLock()
{
	xSemaphoreTake(xMemoryMutex, portMAX_DELAY);
}
//------------------------------------------------------------------------------
void xMemoryUnLock()
{
	xSemaphoreGive(xMemoryMutex);
}
//------------------------------------------------------------------------------
void xMemoryInit()
{
	xMemoryMutex = xSemaphoreCreateMutex();
}
